    <h1>PMP Civil Subpoena Policy</h1>
    <p>
        PMP's Privacy Policy prohibits the release of customer or account information except in limited circumstances,
        including with express permission from the customer, as and when required or permitted by law, to conform to
        the edicts of the law, or to comply with legal process properly served on PMP or one of its affiliates.
    </p>
    <p>
        If you seek the identity or account information of a PMP customer in connection with a civil legal matter,
        you must fax, mail, or serve PMP with a valid subpoena.
    </p>
    <p>
        <strong>Submission of Subpoenas</strong><br/>
        Dotster is located in Burlington, Massachusetts and all civil subpoenas should be served at that location or mailed or faxed to:
    </p><br/><br/>
    <p>
        <strong>
            <span class="label label-important">IMPORTANT NOTE :</span> IN THE EVENT YOU SEND US A NOTICE OF ANY KIND
            VIA EMAIL AND DO NOT RECEIVE A RESPONSE FROM US, PLEASE SUBMIT A DUPLICATE COPY VIA PAPER AND/OR FAX (FAX # 781-998-8277).
            DUE TO THE VAGARIES OF THE INTERNET, AND EMAIL COMMUNICATION IN PARTICULAR, INCLUDING WITHOUT LIMITATION THE
            BURDENS OF SPAM AND THE OCCASIONAL, UNINTENDED EFFECTS OF SPAM FILTERS, SENDING AN ALTERNATE FORM OF NOTICE
            (VIA PAPER AND/OR FAX), WILL HELP ASSURE THAT YOUR NOTICE WILL BE RECEIVED BY US AND ACTED ON IN A TIMELY MANNER.
        </strong>
    </p><br/></br>
    <p>
        Upon the receipt of a validly issued civil subpoena, PMP will promptly notify the customer whose information is
        sought via e-mail or mail. If the circumstances do not amount to an emergency, PMP will not immediately produce
        the customer's information sought by the subpoena and will provide the customer an opportunity to move to quash the subpoena in court.
        And if the customer presents PMP with notice of having filed a motion to quash the subpoena or having sought
        similar protection from a court. PMP reserves the right to object to the subpoena on any applicable grounds.
    </p><br/>
    <p>
        <strong>Fees for Subpoena Compliance</strong>
